public class Variaveis {

    public static void main(String[] args){

        // VARIAVEIS SAO ESPACOS NA MEMORIA
        // USAMOS PARA ARMAZENAR DADOS
        //var num = 3;

        // CRIANDO UMA VARIAVEL DO NOME DO TIPO STRING
        String nome = "Kauet";

        // CRIANDO UMA VARIAVEL  NUM1 DO TIPO INT (INTEIRO)
        int num1 = 5;

        // CRIANDO UMA VARIAVEL NUM2 DO TIPO DOUBLE (NUMEROS COM CASA DECIMAIS)
        double num2 = 3.33;

        // CRIANDO UMA VARIAVEL ISBOY DO TIPO BOOLEAN (TRUE/FALSE
        boolean IsBoy = true;

        // TROCANDO O VALOR DE NUM1 (ANTES 5) PARA 6
        num1 = 6;


        // IMPRIMINDO TODAS AS VARIAVEIS
        System.out.println(nome);

        System.out.println(num1);

        System.out.println(num2);

        System.out.println(IsBoy);


    }
}
